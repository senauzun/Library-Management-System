    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        setVisible(false);
        Home ob = new Home();
        ob.setVisible(true);

        String id = jTextField1.getText();
        String name = jTextField2.getText();
        String publisher = jTextField3.getText();
        String edition = jComboBox1.getSelectedItem().toString();
        String price = jTextField4.getText();
        String pages = jTextField5.getText();
        String stock=jTextField6.getText();

        Connection conn = null;
        PreparedStatement pstmt = null;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/create?useSSL=false", "root", "kaminopain");
            pstmt = conn.prepareStatement("insert into NewBook values (?,?,?,?,?,?,?)");

            pstmt.setString(1, id);
            pstmt.setString(2, name);
            pstmt.setString(3, publisher);
            pstmt.setString(4, edition);
            pstmt.setString(5, price);
            pstmt.setString(6, pages);
            pstmt.setString(7, stock);

            int i = pstmt.executeUpdate();
            if (i > 0) {
                JOptionPane.showMessageDialog(null, "New book added");
            } else {
                JOptionPane.showMessageDialog(null, "Not Saved");

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }


    }//GEN-LAST:event_jButton1ActionPerformed

